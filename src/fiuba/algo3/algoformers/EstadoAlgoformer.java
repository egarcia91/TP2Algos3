package fiuba.algo3.algoformers;

public class EstadoAlgoformer{
	private int vida;
	private int velocidad;
	private int fuerzaAtaque;
	private int distanciaAtaque;
	
	public int getVida(){
		return vida;
	}
	
	public int getVelocidad(){
		return velocidad;
	}
	
	public int getFuerzaAtaque(){
		return fuerzaAtaque;
	}
	
	public int getDistanciaAtaque(){
		return distanciaAtaque;
	}
	
	public void setVida(int vida){
		this.vida = vida;
	}
	
	public void setVelocidad(int velocidad){
		this.vida = velocidad;
	}
	
	public void setFuerzaAtaque(int fuerzaAtaque){
		this.vida = fuerzaAtaque;
	}
	
	public void setDistanciaAtaque(int distanciaAtaque){
		this.vida = distanciaAtaque;
	}	
}
